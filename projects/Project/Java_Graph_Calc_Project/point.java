public class point {
	private double x;
	private double y;
	public point(double a,double b) {
		x = a;
		y = b; 
	}
	
	public double getX() { //returns x coordinate 
		return x;
	}
	public double getY() { //returns y coordinate 
		return y;
	}
	

}
